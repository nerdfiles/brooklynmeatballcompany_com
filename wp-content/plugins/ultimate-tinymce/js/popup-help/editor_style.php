<span class="title" style="padding:0px 5px 0px 5px;background-color:#CCFCD9;border:1px solid #339966;"><strong>Editor Styles</strong></span>
<br />
This option will enable (if available) the current active themes editor-style.css file.  This file will load into the initialization of the tinymce content editor.  This stylesheet is typicaly written to replicate the stylesheet used on the front-end of the website.

<br /><br />

<span class="title" style="padding:0px 5px 0px 5px;background-color:#CCFCD9;border:1px solid #339966;"><strong>Usage:</strong></span>
<br />
Enabling this option will load the themes editor_style.css file.  There are a few notes to be aware of:<br />
<ul>
<li>
Note 1: The file MUST be named exactly <strong>editor_style.css</strong>.
</li><li>
Note 2: If a child theme is active, the <strong>editor_style.css</strong> file MUST be located somewhere in the child theme.
</li><li>
Note 3: This option ONLY affects the tinymce content editor.  It makes NO changes whatsoever to the styles on the front end of the website.
</li><li>
Note 4: It is most likely a manual refresh of the browsers cache will need to be performed before changes may be seen.  This option can usually be found in the browsers "File", "Settings" menu.  Use Google, if necessary, to find out how to manually empty the browser cache.  If changes are still not observed, close the browser completely and try again.</ul>

